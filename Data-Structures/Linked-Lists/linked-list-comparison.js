/*

SINGLY VS. DOUBLY LINKED LISTS COMPARISON

SINGLY LINKED LISTS:

    Pros:

        - Easy to implement
        - Less memory required
        - Faster (because you do not need to deal with the prev pointer)
        - Mostly used for fast insertion and deletion but not much traversal (lookups)
    
    Cons:

        - Cannot be traversed in reverse

DOUBLY LINKED LISTS:

    Pros:

        - Can be traversed from the back (tail)
        - Mostly used if operations will be lookup heavy
    
    Cons:

        - More memory required
        - Harder to implement
        - Slower

*/