/*

WHEN TO USE RECURSION:

Recursion Pros:

    - Dry
    - Readable

Recursion Cons:

    - Very memory-heavy because of the growing of the call stack

When to Use Recursion:

- When using a tree or converting something into a tree
- When dealing with a problem that involves dividing the problem into sub-problems that are smaller instances of the same problem (i.e. each instance of the sub-problem is identical in nature to the main problem)
- When the solutions of these smaller sub-problems can be combined to solve the problem at hand

*/